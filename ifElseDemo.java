public class ifElseDemo{
public static void main(String[] args) {
        System.out.println("\n--- If-Else ---");
        int a = 45;
        // to check a is even or odd
        if (a % 2 == 0) {
            System.out.println("a is even");
        } else {
            System.out.println("a is odd");
        }
    }
}
